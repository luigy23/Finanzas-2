    private void limpiar() {

        Tf_Valor.setText("0");
        Tf_Fecha.setText("");
        TF_VT.setText("");
        Ta_Descripción.setText("");
        Tf_Costo.setText("0");
        Tf_Pago.setText("0");

    }
    int valorS;
    int Costo;
    int PagoT;
    int Ganancia;

    private int calcular() {

        int pagoauto = (valorS - Costo) / 2;

        return pagoauto;

    }

    private void ganancia() {

        Costo = Integer.parseInt(Tf_Costo.getText());
        PagoT = Integer.parseInt(Tf_Pago.getText());
        Ganancia = (valorS - Costo - PagoT);
        String Gananciatxt = Integer.toString(Ganancia);



        Label_Ganancia.setText(Gananciatxt);

    }


