    private void BTN_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_guardarActionPerformed
        rellenar();
        ganancia();
        calcular();
        ganancia();

        int resp = JOptionPane.showConfirmDialog(null, "¿Está seguro de crear este reporte?");
        if (resp == 0) {

            String Descripción = Ta_Descripción.getText();
            String V_T = TF_VT.getText();
            String Fecha = Tf_Fecha.getText();
            String tiempo;
            Dat.InsertarIngreso(valorS, Costo, PagoT, Ganancia, Descripción, V_T, Fecha);
            caj.ObtenerDatos();

            int Saldoac = Integer.parseInt(caj.obtenerTextInfo("Saldo"));
            System.out.println("saldo actual= " + Saldoac);
            int ingreso = Saldoac + Ganancia;
            caj.ActualizarCaja(Integer.toString(ingreso));

            System.out.println("Todo bien todo correcto");
            limpiar();
}

    }//GEN-LAST:event_BTN_guardarActionPerformed

